#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	void	*new;

	len = ft_strlen(s1) + 1;
	new = malloc(len);
	if (new == 0)
		return (0);
	ft_strlcpy(new, s1, len);
	return ((char *) new);
}
